class Student:
    def __init__(self ,surname ,university, gender, progress, group="АДЭУ-211"):
        self.surname = surname
        self.university = university
        self.gender = gender
        self.progress = progress
        self.group = group

    def __del__(self):
        print('Отчислен студент -',self.surname,self.progress)

    def info(self):
        return "{0} - {1},{2},{3},{4}".format(self.surname, self.university, self.gender, self.progress, self.group)


