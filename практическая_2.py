class Person:
    def __init__(self, name, surname, qualification = 1):
      self.name = name
      self.surname = surname
      self.qualification = qualification
     
    def display(self):
        return self.name, self.surname, self.qualification

    def __del__(self):
        print('До свидания, мисс {} {}'.format(self.name, self.surname))

